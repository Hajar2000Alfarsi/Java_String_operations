public class StringPositionDemo {
    public static void main(String[] args) {
        String text="Java Programming";
        System.out.println("Original text: " + text);
        System.out.println("index of 'P': " + text.indexOf('P'));
        System.out.println("Does text start with 'J': " + text.startsWith("J"));
        System.out.println("Does text ends with 'Programming': " + text.endsWith("Programming"));


    }
}
